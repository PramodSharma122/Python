
# String Slicing & Operations on string

# Length of a string
names="Pramod,Aabu"
print(len(names))

fruit="banana"
len1=len(fruit)
print(len1)

# string slicing
print(fruit[0:4])
print(fruit[:4])
print(fruit[2:5])
print(fruit[1:])
print(fruit[0:len(fruit)-3]) # length of fruit is 6 and doing 6-3.
print(fruit[-3:-1])